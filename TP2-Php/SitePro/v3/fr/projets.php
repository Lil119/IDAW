            <h4>Projet TIPE</h4>
            <p>Pour le projet TIPE de l'année académique 2021-2022, j'ai entrepris la conception
                d'un protocole expérimental visant à évaluer et comparer la résistance thermique de
                divers matériaux. Ce projet a constitué une exploration approfondie des propriétés
                thermiques des matériaux, mettant en œuvre des techniques de mesure précises et des
                dispositifs expérimentaux spécialisés. La phase de conception du protocole a nécessité
                une analyse critique des facteurs influant sur la résistance thermique, ainsi que la
                sélection judicieuse des matériaux à inclure dans l'étude. À l'issue de la phase
                expérimentale, les résultats ont été analysés de manière approfondie, et des conclusions
                significatives ont été tirées. La présentation orale de 15 minutes devant un jury
                d'enseignants-chercheurs a offert l'occasion de partager les démarches, les défis
                rencontrés, et les découvertes majeures issues de cette étude scientifique approfondie.
                Ce projet a représenté une opportunité stimulante d'appliquer des connaissances
                théoriques à des situations pratiques tout en perfectionnant mes compétences en
                communication scientifique.
            </p>
        </section>

