            <h4 class="text-primary">Projet d'Encouragement des Lycéennes vers les Écoles d'Ingénieur</h4>
            <p>Ma participation active à un projet visant à encourager les lycéennes à
                envisager une carrière dans le domaine de l'ingénierie a été une expérience
                significative et gratifiante. À travers des ateliers interactifs et la participation
                à des forums éducatifs, j'ai eu l'opportunité de partager ma passion pour l'ingénierie,
                d'expliquer les divers domaines et de démystifier les stéréotypes souvent associés à
                cette profession. L'objectif était d'inspirer les lycéennes à explorer les opportunités
                qui s'offrent à elles dans le domaine de l'ingénierie et de les informer sur les
                diverses branches de cette discipline. L'interaction directe avec les élèves a permis
                de répondre à leurs questions, de partager des expériences personnelles et de souligner
                l'importance de la diversité dans le secteur de l'ingénierie. Ce projet a également
                renforcé ma conviction que l'éducation et la sensibilisation sont des leviers puissants
                pour encourager les jeunes talents à envisager des carrières enrichissantes dans les
                sciences et l'ingénierie.</p>
        </section>

        <section class="cadre-orange">
            <h4>Rugby Féminin en Action : Un Tournoi Inter-École</h4>
            <p>En tant qu'initiatrice d'une équipe féminine de rugby, j'ai orchestré la
                réunion de joueuses passionnées et déterminées pour participer à un tournoi
                inter-école. L'objectif était double : créer une opportunité d'expression sportive
                pour les femmes tout en favorisant une atmosphère inclusive au sein du monde du rugby.
                À travers la coordination d'entraînements dédiés et l'encouragement à la collaboration
                au sein de l'équipe, nous avons formé un groupe uni et motivé. Ce projet a été bien plus
                qu'une simple participation à une compétition sportive ; il a symbolisé la volonté de
                diversifier le paysage du rugby, traditionnellement axé sur les équipes masculines. La
                réussite de cette entreprise a mis en avant la capacité des femmes à exceller dans le
                rugby et a contribué à la promotion de la diversité dans ce sport dynamique. La
                participation enthousiaste de notre équipe au tournoi inter-école était une déclaration
                d'autonomie, démontrant que le rugby est un terrain de jeu accessible et accueillant
                pour tous.</p>
